/**
 * Esta función devuelve la suma de dos parametros.
 * @param integer $num1 primer número suma.
 * @param integer $num2 segundo número suma.
 * @since version 1.0
 * @return resultado suma $num1 y $num2.
 */
function suma($num1,$num2){return $num1 + $num2;}
/** 
 * Esta función devuelve la resta de dos parametros.
 * @param integer $num1 primer número resta.
 * @param integer $num2 segundo número resta.
 * @since version 1.0
 * @return resultado resta $num1 y $num2.
 *  
 */
function resta($num1, $num2){return $num1 - $num2;}
